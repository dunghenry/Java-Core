
public class NapChong {
    public static void main(String[] args) {

        System.out.println("Tong nguyen la: "+ Tong(2, 4));
        System.out.println("Tong nguyen la: "+ Tong(2, 2.5)); 
    }
    static int Tong(int a, int b){
        return a + b;
    }
    static double Tong(double a, double b){
        return a + b;
    }
}
